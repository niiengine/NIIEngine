#include "NiiPreProcess.h"
#include "NiiXmlPattern.h"

namespace NII
{
    //------------------------------------------------------------------------
    XmlPattern::XmlPattern() :
        mInit(false)
    {
    }
    //------------------------------------------------------------------------
    XmlPattern::~XmlPattern()
    {
    }
    //------------------------------------------------------------------------
    bool XmlPattern::startup()
    {
        if(!mInit)
        {
            mInit = _init();
        }

        return mInit;
    }
    //------------------------------------------------------------------------
    void XmlPattern::shutdown()
    {
        if(mInit)
        {
            _end();
            mInit = false;
        }
    }
    //------------------------------------------------------------------------
    void XmlPattern::_update()
    {
    }
    //------------------------------------------------------------------------
    PatternID XmlPattern::getPID() const
    {
        return mPatternID;
    }
    //------------------------------------------------------------------------
}