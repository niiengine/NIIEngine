#ifndef _ROOTEVENTPLUGIN_H_
#define _ROOTEVENTPLUGIN_H_

#include "OgrePlugin.h"
#include "OgreSingleton.h"
#include "OgreString.h"

#include "EventContainer.h"

using Ogre::Plugin;
using Ogre::Singleton;
using Ogre::String;

class RootEventPlugin : public Singleton<RootEventPlugin>, Plugin, EventContainer
{
public:
	enum RootEvent
	{
		Initialized,
		Shutdown,
	};
	
	RootEventPlugin();
	virtual ~RootEventPlugin();
		
	const String& getName() const;
	void install();
	void init();
	void shutdown();
	void uninstall();
	
	/** Override standard Singleton retrieval.
	@remarks
	Why do we do this? Well, it's because the Singleton
	implementation is in a .h file, which means it gets compiled
	into anybody who includes it. This is needed for the
	Singleton template to work, but we actually only want it
	compiled into the implementation of the class based on the
	Singleton, not all of them. If we don't change this, we get
	link errors when trying to use the Singleton-based class from
	an outside dll.
	@par
	This method just delegates to the template version anyway,
	but the implementation stays in this single compilation unit,
	preventing link errors.
	*/
	static RootEventPlugin& getSingleton(void);

	/** Override standard Singleton retrieval.
	@remarks
	Why do we do this? Well, it's because the Singleton
	implementation is in a .h file, which means it gets compiled
	into anybody who includes it. This is needed for the
	Singleton template to work, but we actually only want it
	compiled into the implementation of the class based on the
	Singleton, not all of them. If we don't change this, we get
	link errors when trying to use the Singleton-based class from
	an outside dll.
	@par
	This method just delegates to the template version anyway,
	but the implementation stays in this single compilation unit,
	preventing link errors.
	*/
	static RootEventPlugin* getSingletonPtr(void);


protected:
	void registerEvents();
};

#endif // _ROOTEVENTPLUGIN_H_